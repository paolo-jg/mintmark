/**
 * Reads scripts/coins-data.json and patches the dates into src/lib/coins/catalog.ts
 * by matching on series slug similarity.
 *
 * Run: node scripts/merge-coin-dates.mjs
 * Then review the output and commit if it looks good.
 */

import { readFileSync, writeFileSync } from 'fs'

const raw = JSON.parse(readFileSync('scripts/coins-data.json', 'utf8'))

// Build a flat map: usacoinbook slug → dates
const slugToData = {}
for (const cat of raw.catalog) {
  for (const series of cat.series) {
    if (series.dates.length > 0) {
      slugToData[series.slug] = {
        name: series.name,
        category: cat.name,
        dates: series.dates,
      }
    }
  }
}

console.log(`Loaded ${Object.keys(slugToData).length} series with date data\n`)

// Print a summary of what we have
for (const [slug, data] of Object.entries(slugToData)) {
  const total = data.dates.reduce((n, d) => n + d.mintMarks.length, 0)
  console.log(`  ${data.category} / ${data.name} (${data.dates.length} years, ${total} coins)`)
}

// Write as a simple importable JS module for easy integration
const output = `// Auto-generated by scripts/merge-coin-dates.mjs
// Source: usacoinbook.com
// Scraped: ${raw.scraped}

export interface CoinDate {
  year: number
  mintMarks: string[]
}

export interface SeriesDateData {
  name: string
  category: string
  slug: string
  dates: CoinDate[]
}

export const COIN_DATES: Record<string, SeriesDateData> = ${JSON.stringify(
  Object.fromEntries(
    Object.entries(slugToData).map(([slug, data]) => [
      slug,
      { ...data, slug }
    ])
  ),
  null,
  2
)}
`

writeFileSync('src/lib/coins/coin-dates.ts', output)
console.log('\n✓ Written to src/lib/coins/coin-dates.ts')
